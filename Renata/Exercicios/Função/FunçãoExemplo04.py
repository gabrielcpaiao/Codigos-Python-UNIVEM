#Parâmetros
def calcMedia():
    a = int(input("Valor 1 >> "))
    b = int(input("Valor 2 >> "))
    media = (a + b) / 2
    print(f"A média é {media}")

def calcMed(a, b):
    media = (a + b) / 2
    print(f"A média é {media}")

calcMedia()
calcMed(8, 14)
x = 18
y = 10
calcMed(x, y)
calcMed(x, 20)
n1 = float(input("Nota 1 >> "))
