A = int(input("Digite o primeiro valor: "))
B = int(input("Digite o segundo valor: "))
cont = 1
soma = 0
while (cont<=A):
    soma += B
    cont += 1
print(f"O produto entre o primeiro valor digitado e o segundo valor é: {soma}")