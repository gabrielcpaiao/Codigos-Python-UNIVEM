SA, SB, SC = input(). split()
A = float(SA)
B = float(SB)
C = float(SC)
at = (A*C)/2
print("TRIANGULO: {:.3f}".format(at))
ac = (3.14159)*(C**2)
print("CIRCULO: {:.3f}".format(ac))
atra = ((A+B)*C)/2
print("TRAPEZIO: {:.3f}".format(atra))
aqu = B*B
print("QUADRADO: {:.3f}".format(aqu))
aret = A*B
print("RETANGULO: {:.3f}".format(aret))
